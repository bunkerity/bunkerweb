#!/usr/bin/env python3
"""Generate the lego DNS provider registry consumed by the Let's Encrypt plugin.

This is a *maintainer* tool, not shipped at runtime. It reads the per-provider
TOML metadata from a checkout/extract of a pinned `go-acme/lego` release and emits
``src/common/core/letsencrypt/jobs/lego/lego_providers.json``.

The certbot-dns-multi plugin embeds lego via CGO, so the registry MUST be generated
from the exact lego version that the pinned certbot-dns-multi wheel vendors
(certbot-dns-multi 5.2.0 -> lego v5.2.0). Re-run this whenever either pin is bumped.

Usage::

    python3 src/deps/misc/gen_lego_providers.py \
        --lego-src /path/to/lego-5.2.0 \
        --lego-version v5.2.0 \
        --out src/common/core/letsencrypt/jobs/lego/lego_providers.json

The lego source only needs the ``providers/dns/<code>/<code>.toml`` tree; a tarball
extracted with ``--wildcards '*/providers/dns/*/*.toml'`` is enough.

Schema (per lego code)::

    {
      "name": "<Name>",                 # TOML Name (display)
      "credentials_env": [...],         # [Configuration.Credentials] keys (auth vars + aliases)
      "additional_env": [...]           # [Configuration.Additional] keys (propagation/ttl/timeouts)
    }

Required-vs-optional is intentionally NOT derived here: lego's TOML lists every
credential variable (including aliases and alternative auth methods) flatly, so a
mechanical "required" set would be wrong. Early validation for the historical
providers lives in ``legacy_aliases.json`` (authoritative, hand-authored); new
providers are validated as "at least one credential present" plus lego's own
runtime check.
"""

from argparse import ArgumentParser
from json import dump
from pathlib import Path
from sys import exit as sys_exit, stderr

try:
    from tomllib import load as toml_load  # Python 3.11+
except ModuleNotFoundError:  # pragma: no cover - build host is 3.11+
    print("tomllib is required (Python 3.11+)", file=stderr)
    sys_exit(1)


def _keys(section) -> list:
    """Return the ordered keys of a TOML table, or [] when absent/not a table."""
    if isinstance(section, dict):
        return list(section.keys())
    return []


def build_registry(lego_src: Path) -> dict:
    dns_root = lego_src / "providers" / "dns"
    if not dns_root.is_dir():
        print(f"providers/dns not found under {lego_src}", file=stderr)
        sys_exit(1)

    registry = {}
    for toml_path in sorted(dns_root.glob("*/*.toml")):
        # Canonical metadata file is providers/dns/<code>/<code>.toml.
        if toml_path.stem != toml_path.parent.name:
            continue
        with toml_path.open("rb") as fh:
            data = toml_load(fh)

        code = (data.get("Code") or toml_path.stem).strip()
        if not code:
            continue

        configuration = data.get("Configuration") or {}
        registry[code] = {
            "name": (data.get("Name") or code).strip(),
            "credentials_env": _keys(configuration.get("Credentials")),
            "additional_env": _keys(configuration.get("Additional")),
        }

    return registry


def main() -> int:
    parser = ArgumentParser(description="Generate lego_providers.json from lego TOML metadata.")
    parser.add_argument("--lego-src", required=True, type=Path, help="Path to an extracted lego release tree.")
    parser.add_argument("--lego-version", default="", help="lego version tag recorded in the output (e.g. v5.2.0).")
    parser.add_argument("--out", required=True, type=Path, help="Output JSON path.")
    args = parser.parse_args()

    registry = build_registry(args.lego_src)

    payload = {
        "_meta": {
            "generated_from": "go-acme/lego",
            "lego_version": args.lego_version,
            "provider_count": len(registry),
            "note": "Generated by src/deps/misc/gen_lego_providers.py. Do not edit by hand.",
        }
    }
    payload.update(dict(sorted(registry.items())))

    args.out.parent.mkdir(parents=True, exist_ok=True)
    with args.out.open("w", encoding="utf-8") as fh:
        dump(payload, fh, indent=2, ensure_ascii=False, sort_keys=False)
        fh.write("\n")

    print(f"Wrote {len(registry)} providers to {args.out} (lego {args.lego_version or 'unknown'})")
    return 0


if __name__ == "__main__":
    sys_exit(main())
